package ouc.musi.domain;

public class Album {

	private String albm_id;
	private String albm_name;
	private String albm_cvr;
	private String albm_sngr;
	private String albm_date;
	public String getAlbm_id() {
		return albm_id;
	}
	public void setAlbm_id(String albm_id) {
		this.albm_id = albm_id;
	}
	public String getAlbm_name() {
		return albm_name;
	}
	public void setAlbm_name(String albm_name) {
		this.albm_name = albm_name;
	}
	public String getAlbm_cvr() {
		return albm_cvr;
	}
	public void setAlbm_cvr(String albm_cvr) {
		this.albm_cvr = albm_cvr;
	}
	public String getAlbm_sngr() {
		return albm_sngr;
	}
	public void setAlbm_sngr(String albm_sngr) {
		this.albm_sngr = albm_sngr;
	}
	public String getAlbm_date() {
		return albm_date;
	}
	public void setAlbm_date(String albm_date) {
		this.albm_date = albm_date;
	}
	
}
