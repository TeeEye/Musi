package ouc.musi.domain;

public class Music {
	
	private String msc_id;
	private String msc_name;
	private String msc_sngr;
	private String msc_albm;
	private int msc_hot;
	private String msc_pth;
	private String msc_lnth;
    
	public String getMsc_id() {
		return msc_id;
	}
	public void setMsc_id(String msc_id) {
		this.msc_id = msc_id;
	}
	public String getMsc_name() {
		return msc_name;
	}
	public void setMsc_name(String msc_name) {
		this.msc_name = msc_name;
	}
	public String getMsc_sngr() {
		return msc_sngr;
	}
	public void setMsc_sngr(String msc_sngr) {
		this.msc_sngr = msc_sngr;
	}
	public String getMsc_albm() {
		return msc_albm;
	}
	public void setMsc_albm(String msc_albm) {
		this.msc_albm = msc_albm;
	}
	public int getMsc_hot() {
		return msc_hot;
	}
	public void setMsc_hot(int msc_hot) {
		this.msc_hot = msc_hot;
	}
	public String getMsc_pth() {
		return msc_pth;
	}
	public void setMsc_pth(String msc_pth) {
		this.msc_pth = msc_pth;
	}
	public String getMsc_lnth() {
		return msc_lnth;
	}
	public void setMsc_lnth(String msc_lnth) {
		this.msc_lnth = msc_lnth;
	}
    
}
